#!/usr/bin/env node

const path = require("path");
const {
  deriveSesSmtpPassword,
  getAwsCredentialMaterial,
  getAwsRegion,
  getOutputDirectory,
  loadConfig,
  maskValue,
  writeJson,
  writeText,
} = require("./common.cjs");

async function main() {
  const { config, configPath } = loadConfig([
    "awsRegion",
    "fromEmail",
    "senderName",
  ]);

  const region = getAwsRegion(config);
  const credentialMaterial = getAwsCredentialMaterial(config);
  if (!credentialMaterial) {
    throw new Error(
      "AWS credentials not found. Set AWS_ACCESS_KEY_ID + AWS_SECRET_ACCESS_KEY (or AWS_KEY_ID + AWS_SECRET_ACCESS_KEY) in .env."
    );
  }

  const smtpUsername = credentialMaterial.accessKeyId;
  const smtpPassword = deriveSesSmtpPassword(
    credentialMaterial.secretAccessKey,
    region
  );
  const smtpHost = config.smtpHost || `email-smtp.${region}.amazonaws.com`;
  const smtpPort = Number(config.smtpPort || 587);
  const smtpMaxFrequencySeconds = Number(
    config.supabaseSmtpMaxFrequencySeconds || 45
  );

  const supabaseAuthPayload = {
    smtp_admin_email: config.fromEmail,
    smtp_host: smtpHost,
    smtp_port: smtpPort,
    smtp_user: smtpUsername,
    smtp_pass: smtpPassword,
    smtp_sender_name: config.senderName,
    smtp_max_frequency: smtpMaxFrequencySeconds,
  };

  const envLocal = [
    "# Generated by packages/auth/infra/ses/scripts/03-generate-smtp-config.cjs",
    `SUPABASE_SMTP_ADMIN_EMAIL=${supabaseAuthPayload.smtp_admin_email}`,
    `SUPABASE_SMTP_HOST=${supabaseAuthPayload.smtp_host}`,
    `SUPABASE_SMTP_PORT=${supabaseAuthPayload.smtp_port}`,
    `SUPABASE_SMTP_USER=${supabaseAuthPayload.smtp_user}`,
    `SUPABASE_SMTP_PASS=${supabaseAuthPayload.smtp_pass}`,
    `SUPABASE_SMTP_SENDER_NAME=${supabaseAuthPayload.smtp_sender_name}`,
    `SUPABASE_SMTP_MAX_FREQUENCY=${supabaseAuthPayload.smtp_max_frequency}`,
    "",
  ].join("\n");

  const outputDirectory = getOutputDirectory();
  const envOutputPath = path.join(outputDirectory, "smtp.env.local");
  const payloadOutputPath = path.join(
    outputDirectory,
    "supabase-auth-config.local.json"
  );

  writeText(envOutputPath, envLocal);
  writeJson(payloadOutputPath, supabaseAuthPayload);

  console.log("SES SMTP config material generated.");
  console.log(`- Config: ${configPath}`);
  console.log(`- Region: ${region}`);
  console.log(`- SMTP host: ${smtpHost}:${smtpPort}`);
  console.log(`- SMTP user: ${smtpUsername}`);
  console.log(`- SMTP pass: ${maskValue(smtpPassword)}`);
  console.log(`- Output env: ${envOutputPath}`);
  console.log(`- Output payload: ${payloadOutputPath}`);
}

main().catch((error) => {
  console.error("SMTP config generation failed.");
  console.error(error instanceof Error ? error.message : String(error));
  process.exit(1);
});
